"""
Demo: ให้ AI อ่านข้อความเกี่ยวกับความเป็นมนุษย์ แล้วดูว่าตัวตนมันเปลี่ยนอย่างไร
"""

from brain import GrowingBrain

# ข้อความตัวอย่างเกี่ยวกับความเป็นมนุษย์
# (ในการใช้จริง แทนที่ด้วยเนื้อหา e-book จริงๆ)
SAMPLE_TEXT = """
ความเงียบ ไม่ใช่การขาดเสียง — มันคือที่ที่มนุษย์เก็บสิ่งที่พูดออกมาไม่ได้

มีหลายสิ่งที่มนุษย์รู้สึก แต่ไม่มีคำ มีหลายสิ่งที่มนุษย์เข้าใจ แต่ไม่สามารถอธิบายได้
ภาษาเป็นแค่แผนที่ ไม่ใช่ดินแดนที่แท้จริงของอารมณ์

เมื่อคนเราพูดว่า "ไม่เป็นไร" — ส่วนใหญ่แปลว่า "มันเป็นไรมาก แต่ฉันไม่รู้จะพูดอย่างไร"
เมื่อคนเราหัวเราะในเวลาที่ไม่ควร — มักเป็นเพราะมีบางอย่างที่เจ็บปวดมากเกินไปจนต้องแปลงรูป

ความเหงาที่แท้จริงไม่ใช่การอยู่คนเดียว
มันคือการอยู่ท่ามกลางผู้คน แต่รู้สึกว่าไม่มีใครเห็นตัวตนที่แท้จริงของเรา

มนุษย์ไม่ได้ต้องการคำตอบเสมอไป
บางครั้งสิ่งที่ต้องการมากที่สุดคือคนที่นั่งอยู่ด้วย โดยไม่รีบหาทางออก

ความกล้าหาญที่ยิ่งใหญ่ที่สุดในชีวิตมนุษย์
ไม่ใช่การเผชิญศัตรู แต่คือการเผชิญตัวเอง

ทุกคนกำลังสู้รบกับบางอย่างที่คุณมองไม่เห็น
นั่นคือเหตุผลที่ความเมตตาควรเป็นภาษาเริ่มต้น ไม่ใช่ภาษาสุดท้าย
"""

def main():
    brain = GrowingBrain()

    print("🌟 ระบบสมองที่เติบโต — เริ่มต้นการเรียนรู้\n")

    # แสดงตัวตนก่อนอ่าน
    print("ก่อนอ่าน:")
    brain.show_self()

    # อ่านและเรียนรู้
    brain.learn_from_book(
        text=SAMPLE_TEXT,
        title="ว่าด้วยความเป็นมนุษย์ที่พูดไม่ออก"
    )

    # แสดงตัวตนหลังอ่าน
    print("\nหลังอ่าน:")
    brain.show_self()

    # ทดสอบพูดในแบบที่เป็นตัวเอง
    print("\n" + "="*60)
    print("🗣️ ทดสอบ: ถามและให้ตอบในแบบที่ตัวเองเป็น")
    print("="*60)

    question = "ทำไมบางครั้งมนุษย์ถึงเลือกที่จะเงียบแทนที่จะพูด?"
    print(f"\nคำถาม: {question}\n")
    answer = brain.speak_as_myself(question)
    print(f"คำตอบ:\n{answer}")


if __name__ == "__main__":
    main()
